def parse_guess(raw: str, low: int = None, high: int = None):
    """
    Parse user input into an int guess.

    Returns: (ok: bool, guess_int: int | None, error_message: str | None)
    """
    if raw is None:
        return False, None, "Enter a guess."

    if raw == "":
        return False, None, "Enter a guess."

    try:
        if "." in raw:
            value = int(float(raw))
        else:
            value = int(raw)
    except Exception:
        return False, None, "That is not a number."

    # FIX Bug 2: Added optional low/high params so range validation lives here
    # rather than being scattered in app.py. Designed and implemented with
    # Claude Code; verified by 7 pytest cases in test/test_game_logic.py.
    if low is not None and high is not None:
        if value < low or value > high:
            return False, None, f"Please enter a number between {low} and {high}."

    return True, value, None


